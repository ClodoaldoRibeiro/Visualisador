    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Arquivo de imagem", "bmp", "png", "jpg"));
        fileChooser.setDialogTitle("Abrir imagem");
        fileChooser.setPreferredSize( new Dimension(614,397));
        
        if (fileChooser.showOpenDialog(jMenu1) == JFileChooser.APPROVE_OPTION){
            File f = fileChooser.getSelectedFile();
            label.setIcon( new ImageIcon( f.toString() ));
            label.setHorizontalAlignment(label.CENTER);
            jp.getViewport().add(label);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

